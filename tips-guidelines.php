                    <h3>Moving a house</h3>
                    <p class="gray lh17em mb15px text-justify">When you move your house it is not just about relocating
                        from one place to another but beginning a new chapter in your life. You should look forward to
                        all the wonderful things that are going to happen ahead and not worry about the process of
                        shifting your home. It is a fact that you will be stressed out when you plan to move but if you
                        leave it to the experts you need not worry about a thing!</p>
                    <h4>Things to keep in mind:</h4>
                    <ul class="list gray">
                        <li>Make sure that you have made the flight or train reservation for the entire family in
                            advance.</li>
                        <li>Keep valuables and important items separately so that they don’t get misplaced during
                            packing.</li>
                        <li>Inform all the concerned organizations and persons about the relocation.</li>
                        <li>Note down the contact details of friends and relatives.</li>
                        <li>Empty the refrigerator contents and clean it up.</li>
                        <li>Backup the data in your PC and laptop before packing it.</li>
                        <li>Remove the items no longer needed and keep valuables safe.</li>
                        <li>Switch off the power supply before you leave. </li>
                        <li>Lock the entire property and hand over the keys to the authorized person.</li>
                    </ul>
                    <hr>
                    <h3>Pet Relocation</h3>
                    <p class="gray lh17em mb15px mt15px text-justify">Relocating a pet can be hard for you as well as
                        your pet. Pets are family and you would not want to take any chances with their well-being.
                        Therefore, you would be on a lookout for the best <strong>pet movers</strong>. We at Mr.Move
                        have the much-needed knowledge and expertise to transport your pet to the new destination.</p>
                    <h4>Things to keep in mind</h4>
                    <ul class="list gray">
                        <li>Your pet’s checkup needs to be done in advance. All the necessary documents like vaccination
                            records and health certificates have to be kept carefully.</li>
                        <li>Provide all your details on the pet’s collar along with the special care instructions.</li>
                        <li>Before the day of the journey get your pet accustomed to traveling in automobiles.</li>
                        <li>Don’t feed your pet 7 hours before the journey. Keep water close by.</li>
                        <li>If traveling by air, make sure that the airline’s crew is directed properly and the pet has
                            a comfortable carrier to sit in.</li>
                    </ul>
                    <hr>
                    <h3>Office relocation</h3>
                    <p class="gray lh17em mb10px text-justify">Building your brand and reputation takes a lot of effort
                        and we at Mr.Move understand that. Our corporate relocation services ensure that there is
                        minimum downtime so that your business is back to normal faster and your productivity is not
                        affected.</p>
                    <h4>Things to keep in mind</h4>
                    <ul class="list gray">
                        <li>You need to book flight tickets for the staff as soon as the day of shifting the office is
                            decided.</li>
                        <li>Assign different duties to different individuals. </li>
                        <li>Find a reliable person and give him the responsibility of the confidential documents. </li>
                        <li>Keep all the emergency supplies within reach during the journey.</li>
                        <li>Ensure that you have taken the backup of all the office PCs and laptops before you leave.
                        </li>
                        <li>Make sure that nothing remains in the old office.</li>
                        <li>Shut down the power supply completely before you go.</li>
                        <li>Ensure that the IT equipment is safe.</li>
                        <li>Hand over the keys of the workstations and cabinets to the employees.</li>
                        <li>Keep the confidential files safely.</li>
